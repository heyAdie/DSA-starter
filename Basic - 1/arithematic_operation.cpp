
#include <iostream>
using namespace std;
int main()
{
    int x = 5;
    int y = 2;
    cout << x + y << endl;
    cout << x - y << endl;
    cout << x / y << endl;
    cout << x % y << endl;
    cout << x * y << endl;
    cout << endl;

     // increment and decrement :

int p = 5;
    cout<<p<<endl;
    ++p;     // pre increment : x = x + 1
    cout<<p<<endl;
    p++;     // post increment :
    cout<<p<<endl;
    p--;     // x = x - 1
    cout<<p<<endl;
    --p;     // x = x - 1
    cout<<p<<endl;
    
}